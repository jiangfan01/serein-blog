// ============================================================
// 怎么"认出"该用双指针？（识别信号 —— 比解法更重要）
// ============================================================
// 出现下面任一信号，先想双指针：
//   1. 数组/字符串是【有序】的，让你找"两个数满足某条件"
//   2. 让你"原地"操作、去重、移动元素（快慢指针）
//   3. 找回文、反转、两端向中间靠
//   4. 暴力解是 O(n²) 的"两层嵌套循环找一对"
//
// 核心思想：用两个指针代替两层循环，把 O(n²) 压到 O(n)。
// 之所以能压，是因为【有序】给了你"该往哪边动"的依据。

// ============================================================
// 模板 A：左右指针向中间夹逼（用于有序数组找一对）
// ============================================================
// 例题：两数之和 II（输入有序数组），返回和为 target 的两个数的下标
function twoSumSorted(nums: number[], target: number): [number, number] | null {
  let left = 0;
  let right = nums.length - 1;

  while (left < right) {
    const sum = nums[left] + nums[right];
    if (sum === target) {
      return [left, right]; // 找到了
    } else if (sum < target) {
      left++; // 和太小 → 左边右移，换个更大的数
    } else {
      right--; // 和太大 → 右边左移，换个更小的数
    }
  }
  return null;
}

// ☝️ 整个"灵光一闪"就这三行决策：
//   sum < target → left++（要更大）
//   sum > target → right--（要更小）
// 因为数组有序，每次都能稳稳排除一个方向，绝不漏解。

// ============================================================
// 模板 B：快慢指针（用于原地去重/移动）
// ============================================================
// 例题：原地删除有序数组中的重复项，返回去重后的长度
function removeDuplicates(nums: number[]): number {
  if (nums.length === 0) return 0;
  let slow = 0; // slow 指向"已经处理好的最后一个位置"
  for (let fast = 1; fast < nums.length; fast++) {
    if (nums[fast] !== nums[slow]) {
      slow++;
      nums[slow] = nums[fast]; // 把新的不重复元素搬到 slow 处
    }
  }
  return slow + 1;
}

// ============================================================
// 自测
// ============================================================
console.log("两数之和 II:");
console.log("  [2,7,11,15] target=9 →", twoSumSorted([2, 7, 11, 15], 9)); // [0,1]
console.log("  [1,3,4,5,7] target=11 →", twoSumSorted([1, 3, 4, 5, 7], 11)); // [2,4] (4+7)
console.log("  [1,2,3] target=100 →", twoSumSorted([1, 2, 3], 100)); // null

const arr = [1, 1, 2, 2, 3];
const len = removeDuplicates(arr);
console.log("\n原地去重:");
console.log("  长度 =", len, " 前 len 个 =", arr.slice(0, len)); // 3, [1,2,3]

