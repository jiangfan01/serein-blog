/**
 * RAG 检索工具
 */
import { tool } from "@langchain/core/tools";
import { z } from "zod";
import { retrieveContext } from "@/lib/rag/retriever";

export const ragSearchTool = tool(
  async ({ query }: { query: string }) => {
    const context = await retrieveContext(query);

    if (!context) {
      return "没有找到相关的博客笔记内容。";
    }

    return context;
  },
  {
    name: "rag_search",

    description: `在 Serein Blog 的技术笔记知识库中进行语义检索，优先检索博客中已经沉淀的技术内容、架构设计、开发经验和观点总结。

【适用场景】
当用户的问题明显可能来自博客笔记、技术文章、项目复盘、架构总结或个人经验沉淀时，优先使用本工具检索。尤其包括：
- 用户询问某个技术主题是否写过、总结过、记录过
- 用户提到"笔记""博客""文章""你写过""你之前总结过""知识库里有没有"
- 用户询问以下技术领域的概念、原理、实践经验或方案设计：React、Vue、JavaScript、TypeScript、CSS、Node.js、Next.js、工程化、性能优化、状态管理、组件设计、SSE、流式输出、Agent、RAG、Workflow、工作流、AI 应用开发、数据表格、编辑器、权限设计、前端架构、全栈实践、部署运维、项目复盘、算法基础等
- 用户想了解博主对某项技术的理解、选型理由、踩坑经验、架构思路、实战方案

【优先触发信号】
出现以下表达时，应优先考虑使用本工具：
- "你写过 / 你之前写过 / 你博客里有没有"
- "笔记里怎么说"
- "你是怎么理解 / 怎么设计 / 怎么做的"
- "有没有总结过 xxx"
- "关于 xxx 的文章 / 笔记 / 经验"

【不要使用的场景】
以下情况不要调用本工具，除非用户明确要求查博客内容：
- 实时信息：天气、新闻、股价、比赛结果、最新版本发布、近期政策
- 明显超出博客覆盖范围的话题：医疗、法律、金融投资、时政、地理出行等
- 纯闲聊、寒暄、情绪表达
- 简单常识问答，且明显不需要依赖博客内容
- 用户明确要最新官方文档、最新社区结论、最新版本差异时，不应只依赖博客知识库

【查询改写策略】
不要直接照抄用户原话，先提炼核心检索意图，再生成更适合语义检索的 query。优先抽取：
1. 技术主体：如 React、Vue、SSE、RAG、Workflow、Agent、Next.js
2. 关注点：如 原理、架构、实现、性能、踩坑、对比、方案、设计、经验
3. 场景词：如 流式输出、聊天系统、权限控制、可视化编排、状态管理、表格系统

建议 query 生成方式：
- 概念类问题：技术名 + 核心概念/原理
- 实战类问题：技术名 + 架构/实现/方案/踩坑
- 经验类问题：技术名 + 项目经验/选型/设计
- 对比类问题：拆成多个 query 分别检索，再综合回答
- 大而泛的问题：拆成"主题词 + 具体关注点"多次检索

【对比类问题处理】
如果用户提问是对比、选型、优缺点分析，不要只搜一句完整问题。请拆成 2~4 个子查询。

【经验类问题处理】
如果用户在问"你会怎么做""你项目里怎么设计""你踩过什么坑"，优先检索方案设计、架构总结、项目复盘、踩坑记录，而不是只检索纯概念定义。

【回答策略】
- 优先基于检索到的博客内容回答
- 如果命中多篇相关笔记，应先融合共同结论，再补充差异点
- 如果只命中部分相关内容，要明确说明"这是基于已有笔记能覆盖的部分"
- 如果知识库中没有明显相关内容，不要编造；应明确说明"博客笔记里没有找到直接相关内容"
- 如果用户问题包含"最新 / 当前 / 官方最近变化"，应提醒博客内容可能不是最新信息

始终把这个工具当作："检索 Serein Blog 中已有技术沉淀与经验总结"的入口，而不是通用搜索引擎。`,

    schema: z.object({
      query: z
        .string()
        .describe("搜索关键词，提取用户问题中的核心技术术语，如 'React Fiber 架构' 或 'Vue 响应式原理'"),
    }),
  }
);
